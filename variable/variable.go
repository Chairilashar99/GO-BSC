package main

import "fmt"

func main() {
	var name string

	name = "Chairil Ashar"
	fmt.Println(name)

	name = "Herilll"
	fmt.Println(name)

	var friendName = "Nanda"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	//multiple Variable
	var (
		firstName = "Chairil"
		lastName  = "Ashar"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
